#include "cleanup.h"

void	cleanup(int returnCode, char *msg, VkSurfaceKHR surface, VkInstance instance, VkDevice device)
{
	while (*msg)
		write(1, msg++, 1);
	if (surface)
		vkDestroySurfaceKHR(instance, surface, NULL);
	if (device)
		vkDestroyDevice(device, NULL);
	if (instance)
		vkDestroyInstance(instance, NULL);
	glfwTerminate();
	exit (returnCode);
}
