#include "initWindow.h"

GLFWwindow	*initWindow()
{
	GLFWwindow	*window;

	glfwInit();
	glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);
	glfwWindowHint(GLFW_RESIZABLE, GLFW_FALSE);
	window = glfwCreateWindow(WIDTH, HEIGHT, "Vulkan", NULL, NULL);
	return (window);
}
